# 파이썬에서 입력받기
# 파이썬에서 입력받을때는 input()사용
# 변수 여러개의 값을 입력받으려면 split()사용 

# # # 더하기 
num1,num2=map(int,input().split())
print(num1+num2)

# # # 빼기 
num1,num2=map(int,input().split())
print(num1-num2)

# # 곱하기 
num1,num2=map(int,input().split())
print(num1*num2)

# 나누기 
num1,num2=map(int,input().split())
print(num1/num2)
